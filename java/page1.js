jQuery(function ($) {

var $window = $(window),
    $w1 = $('.w1');


function resize() {
    if ($window.width() > 368.8) {
            $w1.removeClass("w11");
            $w1.addClass("w1");

    } else if ($window.width() < 368.8) {
            $w1.addClass("w11");
            $w1.removeClass("w1");}
        
 }

 $window.resize(resize)
        .trigger('resize');
});

